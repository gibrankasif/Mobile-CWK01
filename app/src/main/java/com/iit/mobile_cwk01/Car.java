package com.iit.mobile_cwk01;

public class Car {
    private int carImg;
    private String carMake;

    public Car (int carImg, String carMake) {
        this.carImg = carImg;
        this.carMake = carMake;
    }

    public int getCarImg() {
        return carImg;
    }

    public String getCarMake() {
        return carMake;
    }

}
